    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
    int selectedRow = jTablePacientes.getSelectedRow();

if (selectedRow != -1) {
    int pacienteId = Integer.parseInt(jTablePacientes.getValueAt(selectedRow, 0).toString());

    System.out.println("ID del paciente seleccionado: " + pacienteId);

    PacienteDAO dao = new PacienteDAO();
    Paciente paciente = dao.obtenerPacientePorId(pacienteId);

    if (paciente != null) {
        Menu_Admin_Pacientes_Modificar ventanaModificar = new Menu_Admin_Pacientes_Modificar(paciente);
        escritorio.add(ventanaModificar);
        ventanaModificar.setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "No se encontr√≥ al paciente.");
    }
} else {
    JOptionPane.showMessageDialog(this, "Por favor, selecciona un paciente de la tabla.");
}

    }//GEN-LAST:event_btnModificarActionPerformed

